package fr.agefospme.referentiel.individu.service.exception;

public class GestionIndividuNomAssocieIncorrectException extends Exception {

	private String code;

	public GestionIndividuNomAssocieIncorrectException() {
	}

	public GestionIndividuNomAssocieIncorrectException(String code,
			String message) {
		super(message);
		this.code = code;
	}

	public GestionIndividuNomAssocieIncorrectException(String message,
			Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GestionIndividuNomAssocieIncorrectException(String message,
			Throwable cause) {
		super(message, cause);
	}

	public GestionIndividuNomAssocieIncorrectException(String message) {
		super(message);
	}

	public GestionIndividuNomAssocieIncorrectException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
