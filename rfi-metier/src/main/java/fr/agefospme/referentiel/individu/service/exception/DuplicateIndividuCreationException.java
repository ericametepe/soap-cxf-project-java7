package fr.agefospme.referentiel.individu.service.exception;

public class DuplicateIndividuCreationException extends Exception {
	
	private String code;



	public DuplicateIndividuCreationException() {
		
	}

	public DuplicateIndividuCreationException(String message, String code) {
		super(message);
		this.code=code;
	}

	public DuplicateIndividuCreationException(Throwable cause) {
		super(cause);
	}

	public DuplicateIndividuCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public DuplicateIndividuCreationException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
	
	public DuplicateIndividuCreationException(String message, String code, Throwable cause) {
		super(message, cause);
	}
	
	
	
	
	
	
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;



	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
