package fr.agefospme.referentiel.individu.service.exception;

public class GestionIndividuChampMalFormateException extends Exception {

	
	private String code;



	public GestionIndividuChampMalFormateException() {
	}

	public GestionIndividuChampMalFormateException(String code, String message) {
		super(message);
		this.code = code;
	}

	public GestionIndividuChampMalFormateException(Throwable cause) {
		super(cause);
	}

	public GestionIndividuChampMalFormateException(String message,
			Throwable cause) {
		super(message, cause);
	}

	public GestionIndividuChampMalFormateException(String message,
			Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;


}
